#include "operations.h"

/**
 * add - addition function
 * @a: parameter
 * @b: parameter
 * Return: a + b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtraction function
 * @a: parameter
 * @b: parameter
 * Return: a - b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mutiplication function
 * @a: parameter
 * @b: parameter
 * Return: a * b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division function
 * @a: parameter
 * @b: parameter
 * Return: a / b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus function
 * @a: parameter
 * @b: parameter
 * Return: a % b
 */

int mod(int a, int b)
{
	return (a % b);
}
